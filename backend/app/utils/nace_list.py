_NACE_RE = re.compile(r"^\s*(\d{2})(?:\.(\d{1,2}))?\s*$")


def normalize_nace(code: str) -> str | None:
    """'62.1' → '62.01' biçimine normalize eder. 4 haneli kod resmi sınıf
    listesinde değilse, 2 haneli bölüm geçerli değilse None döner."""
    if not code:
        return None
    m = _NACE_RE.match(code)
    if not m:
        return None
    division, sub = m.group(1), m.group(2)
    if division not in VALID_NACE_DIVISIONS:
        return None
    if sub is None:
        return division
    full = f"{division}.{int(sub):02d}"
    # Tam (4 haneli) kod resmi sınıf listesinde olmalı
    if full in VALID_NACE_CLASSES:
        return full
    # Geçerli bölüm ama sınıf eşleşmedi → bölümü döndür (kaba ama geçerli)
    return division


def is_valid_nace(code: str) -> bool:
    return normalize_nace(code) is not None


def is_valid_nace_class(code: str) -> bool:
    """Kod TAM olarak resmi 4 haneli bir NACE sınıfı mı?"""
    if not code:
        return False
    m = _NACE_RE.match(code)
    if not m or m.group(2) is None:
        return False
    return f"{m.group(1)}.{int(m.group(2)):02d}" in VALID_NACE_CLASSES
